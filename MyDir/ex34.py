# Accessing elements from lists
# Remember that python doesn't use ordinal numbers such as: 1,2,3
# Rather is uses cardinal ones: 0,1,2,3
# Making it so 0 means the first element of a list.

animals = ['bear', 'python', 'peacock', 'kangaroo', 'whale', 'platypus']

# the animal at 1
print(animals[1])

# the third animal 
print(animals[2])

# the first animal 
print(animals[0])

# the animal at 3
print(animals[3])

# the fifth animal 
print(animals[4])

# the animal at 2
print(animals[2])

# the sixth animal 
print(animals[5])

